// ─── PresetManager ─────────────────────────────────────────────────────────────
//
//  All preset data is stored as flat float arrays.
//  Values are REAL (not normalised) — apply() converts via getNormalisableRange().
//
//  Parameter order (kParamCount = 47):
//  Idx  0- 7: osc1 wave,morph,tune,fine,level,fm,uni,detune
//  Idx  8-15: osc2 wave,morph,tune,fine,level,fm,uni,detune
//  Idx 16-23: osc3 wave,morph,tune,fine,level,fm,uni,detune
//  Idx 24-28: filter cutoff,res,drive,type,env_amt
//  Idx 29-33: amp    attack,decay,sustain,release,curve
//  Idx 34-37: fenv   attack,decay,sustain,release
//  Idx 38-41: lfo1   shape,rate,depth,phase
//  Idx 42-44: lfo2   shape,rate,depth
//  Idx 45-46: master_gain, glide_time
//
//  Bank codes: 0=ANALOG  1=DIGITAL  2=VOCAL  3=BRASS  4=PLUCK  5=ORGAN
//  ANALOG morph frames: 0=Sine 0.14=Soft 0.29=Tri 0.43=Saw8 0.57=Saw16 0.71=FullSaw 0.86=Square 1.0=Pulse
//  Filter type:  0=LadderLP24  1=LadderLP12  2=LadderHP24  3=SVFLP  4=SVFBP

class PresetManager {
public:
    static constexpr int kParamCount = 47;

    static const char* const kParamIds[kParamCount];

    struct Preset {
        const char* category;
        const char* name;
        float v[kParamCount];
    };

    static const std::vector<Preset>& all();

    static juce::StringArray categories();
    static juce::StringArray presetsInCategory(const juce::String& cat);

    static int  indexOf(const juce::String& cat, const juce::String& name);
    static void apply(int idx, juce::AudioProcessorValueTreeState& apvts);

    static int next(int current);
    static int prev(int current);
    static int firstInCategory(const juce::String& cat);

private:
    static std::vector<Preset> buildPresets();
};

