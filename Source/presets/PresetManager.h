// ─── Implementation ──────────────────────────────────────────────────────────
inline const std::vector<PresetManager::Preset>& PresetManager::all() {
    static auto presets = buildPresets();
    return presets;
}

inline juce::StringArray PresetManager::categories() {
    juce::StringArray cats;
    for (auto& p : all())
        if (!cats.contains(p.category)) cats.add(p.category);
    return cats;
}

inline juce::StringArray PresetManager::presetsInCategory(const juce::String& cat) {
    juce::StringArray names;
    for (auto& p : all())
        if (cat == p.category) names.add(p.name);
    return names;
}

inline int PresetManager::indexOf(const juce::String& cat, const juce::String& name) {
    for (int i = 0; i < (int)all().size(); ++i)
        if (juce::String(all()[i].category) == cat && juce::String(all()[i].name) == name) return i;
    return 0;
}

inline int PresetManager::next(int current) {
    return (current + 1) % (int)all().size();
}
inline int PresetManager::prev(int current) {
    return (current - 1 + (int)all().size()) % (int)all().size();
}
inline int PresetManager::firstInCategory(const juce::String& cat) {
    for (int i = 0; i < (int)all().size(); ++i)
        if (juce::String(all()[i].category) == cat) return i;
    return 0;
}

inline void PresetManager::apply(int idx, juce::AudioProcessorValueTreeState& apvts) {
    if (idx < 0 || idx >= (int)all().size()) return;
    const auto& preset = all()[idx];
    for (int i = 0; i < kParamCount; ++i) {
        if (auto* param = apvts.getParameter(kParamIds[i])) {
            auto* rp = dynamic_cast<juce::RangedAudioParameter*>(param);
            if (rp) param->setValueNotifyingHost(
                rp->getNormalisableRange().convertTo0to1(preset.v[i]));
        }
    }
}
